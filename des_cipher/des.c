#define setbit(x,b,v) ((x)|((v)<<(b)))

UINT32 permutation32(UINT32 x)
{
  static int P32[32] = {
    16, 7, 20, 21, 29, 12, 28, 17,
    1, 15, 23, 26, 5, 18, 31, 10,
    2, 8, 24, 14, 32, 27, 3, 9,
    19, 13, 30, 6, 22, 11, 4, 25
  };
  UINT32 r = 0;
  for (int i=0; i<32; i++) {
    r = setbit(r, i, (x>>(P32[i]-1))&1);
  }
  return r;
}

/* permuted choice:
   left:
   57 49 41 33 25 17 9
   1 58 50 42 34 26 18
   10 2 59 51 43 35 27
   19 11 3 60 52 44 36
   right:
   63 55 47 39 31 23 15
   7 62 54 46 38 30 22
   14 6 61 53 45 37 29
   21 13 5 28 20 12 4

   @input: 64-bit input key
   @output: 56-bit round key
*/

UINT64 PC1(UINT64 k)
{
  UINT64 r = 0;
  UINT64 s[7];
  for (int i=0; i<7; i++) {
    UINT64 t = (k>>i)&0x0101010101010101;
    s[i] = t>>56;
    s[i] |= t>>47;
    s[i] |= t>>38;
    s[i] |= t>>29;
    s[i] |= t>>20;
    s[i] |= t>>11;
    s[i] |= t>> 2;
    s[i] |= t<< 7;
    s[i] &= 0xff;
  }
  r |= s[0] | (s[1]<<8) | (s[2]<<16) | ((s[3]<<24)&&0xf);
  r |= (s[6]<<28) | (s[5]<<36) | (s[4]<<44) | ((s[3]>>4)<<52);
  return r;
}

