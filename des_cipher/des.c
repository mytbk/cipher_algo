/* initial permutation:
   58 50 42 34 26 18 10 2
   60 52 44 36 28 20 12 4
   62 54 46 38 30 22 14 6
   64 56 48 40 32 24 16 8
   57 49 41 33 25 17 9 1
   59 51 43 35 27 19 11 3
   61 53 45 37 29 21 13 5
   63 55 47 39 31 23 15 7
*/
UINT64 init_permutation(UINT64 x)
{
  UINT64 mask0 = 0x0101010101010101;
  UINT64 sx[8];
  for (int i=0; i<8; i++) {
    UINT64 t = x & (mask0<<i);
    t <<= (7-i); // move the lowest bit to the 8th bit
    sx[i] = 0;
    sx[i] |= t;
    sx[i] |= t>>9;
    sx[i] |= t>>18;
    sx[i] |= t>>27;
    sx[i] |= t>>36;
    sx[i] |= t>>45;
    sx[i] |= t>>54;
    sx[i] |= t>>63;
    sx[i] &= 0xff;
  }
  return (sx[1])|(sx[3]<<8)|(sx[5]<<16)|(sx[7]<<24)
    |(sx[0]<<32)|(sx[2]<<40)|(sx[4]<<48)|(sx[6]<<56);
}

/* final permutation (invIP):
   40 8 48 16 56 24 64 32
   39 7 47 15 55 23 63 31
   38 6 46 14 54 22 62 30
   37 5 45 13 53 21 61 29
   36 4 44 12 52 20 60 28
   35 3 43 11 51 19 59 27
   34 2 42 10 50 18 58 26
   33 1 41 9 49 17 57 25
*/
UINT64 final_permutation(UINT64 x)
{
  UINT64 mask0 = 0x0101010101010101;
  UINT64 sx[8];
  for (int i=0; i<8; i++) {
    UINT64 t = x & (mask0<<i);
    t <<= (7-i);
    sx[i] = 0;
    sx[i] |= t>>6;
    sx[i] |= t>>12;
    sx[i] |= t>>18;
    sx[i] |= t>>24;
    sx[i] |= t>>39;
    sx[i] |= t>>45;
    sx[i] |= t>>51;
    sx[i] |= t>>57;
    sx[i] &= 0xff;
  }
  return (sx[7])|(sx[6]<<8)|(sx[5]<<16)|(sx[4]<<24)
    |(sx[3]<<32)|(sx[2]<<40)|(sx[1]<<48)|(sx[0]<<56);
}

/* expansion:
   32 1 2 3 4 5
   4 5 6 7 8 9
   8 9 10 11 12 13
   12 13 14 15 16 17
   16 17 18 19 20 21
   20 21 22 23 24 25
   24 25 26 27 28 29
   28 29 30 31 32 1

   @input: 32bit data
   @output: expanded 48bit data, stored in UINT64[0:47]
*/

UINT64 expansion(UINT32 k)
{
  // k[32,1:5]
  UINT64 r = (k>>31)|((k&0x1f)<<1);
  // k[4:9]
  r |= (k&0x1f8)<<3;
  // k[8:13]
  r |= (k&0x1f80)<<5;
  // k[12:17]
  r |= (k&0x1f800)<<7;
  // k[16:21]
  r |= (k&0x1f8000)<<9;
  // k[20:25]
  r |= (k&0x1f80000)<<11;
  // k[24:29]
  r |= (k&0x1f800000)<<13;
  // k[28:32,1]
  r |= ((k&0xf8000000)<<15)|(((UINT64)k&1)<<47);
  return r;
}

